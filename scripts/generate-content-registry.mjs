import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import matter from "gray-matter";
import { compile } from "@mdx-js/mdx";
import rehypeUnwrapImages from "rehype-unwrap-images";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

const CONTENT_DIRS = [
  { dir: "blog", type: "blog" },
  { dir: "til", type: "til" },
  { dir: "cheatsheets", type: "cheatsheet" },
  { dir: "checklists", type: "checklist" },
  { dir: "braindump", type: "braindump" },
];

const BASE_DIR = path.join(ROOT, "content");
const OUT_DIR = path.join(ROOT, "src/lib/content");
fs.rmSync(OUT_DIR, { recursive: true, force: true });
fs.mkdirSync(OUT_DIR, { recursive: true });

function walkDir(dir) {
  const fullPath = path.join(BASE_DIR, dir);
  if (!fs.existsSync(fullPath)) return [];
  const results = [];
  const entries = fs.readdirSync(fullPath, { withFileTypes: true });
  for (const entry of entries) {
    const entryPath = path.join(fullPath, entry.name);
    if (entry.isDirectory()) {
      if (!entry.name.startsWith(".") && !entry.name.startsWith("_")) {
        results.push(...walkDir(path.join(dir, entry.name)));
      }
    } else if (
      (entry.name.endsWith(".mdx") || entry.name.endsWith(".md")) &&
      !entry.name.startsWith("_")
    ) {
      results.push(path.join(dir, entry.name));
    }
  }
  return results;
}

function formatDate(value) {
  if (value instanceof Date) return value.toISOString().slice(0, 10);
  if (typeof value === "string") return value.split("T")[0];
  return "";
}

const allEntries = [];
const compiledMap = {};
let compiled = 0;

for (const { dir, type } of CONTENT_DIRS) {
  const files = walkDir(dir);
  for (const file of files) {
    const filePath = path.join(BASE_DIR, file);
    const raw = fs.readFileSync(filePath, "utf-8");
    const { data, content } = matter(raw);
    const relPath = path.relative(dir, file);
    const slug = relPath.replace(/\.(mdx|md)$/, "").replace(/\\/g, "/");

    const tags = Array.isArray(data.tags) ? data.tags : [];

    // Pre-compile MDX to function-body at build time
    try {
      const result = await compile({ value: content, path: filePath }, {
        outputFormat: "function-body",
        providerImportSource: "@mdx-js/react",
        rehypePlugins: [rehypeUnwrapImages],
      });
      compiledMap[slug] = String(result);
      compiled++;
    } catch (err) {
      console.error(`⚠ Failed to compile ${slug}: ${err.message}`);
    }

    allEntries.push({
      slug,
      title: data.title ?? slug.replace(/-/g, " "),
      date: formatDate(data.date),
      tags,
      excerpt: data.description ?? data.excerpt ?? "",
      type,
      category: data.category ?? data.categories?.[0] ?? "",
    });
  }
}

allEntries.sort((a, b) => {
  const dateA = new Date(a.date).getTime();
  const dateB = new Date(b.date).getTime();
  if (isNaN(dateA) || isNaN(dateB)) return 0;
  return dateB - dateA;
});

// Write registry with compiled sources baked in
const registryPath = path.join(ROOT, "src", "lib", "content-registry.ts");

const output = `// Auto-generated by scripts/generate-content-registry.mjs
// Do not edit manually.

export interface RegistryEntry {
  slug: string;
  title: string;
  date: string;
  tags: string[];
  excerpt: string;
  type: string;
  category: string;
}

export const CONTENT_ENTRIES: RegistryEntry[] = ${JSON.stringify(allEntries)};

// Pre-compiled MDX function bodies — hydrated client-side, no eval() in Workers
export const COMPILED_MAP: Record<string, string> = ${JSON.stringify(compiledMap)};

export const TOTAL_POSTS = ${allEntries.length};
`;

fs.writeFileSync(registryPath, output);
console.log(`✅ Content registry written (${allEntries.length} posts, ${compiled} compiled)`);
